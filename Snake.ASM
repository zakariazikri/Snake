

; SNAKE
CLO
	MOV AL,00
	OUT 08
	OUT 04
;NIM	
	MOV BL, 4A
	MOV [C0], BL
	MOV BL, 33
	MOV [C1], BL
	MOV BL, 44
	MOV [C2], BL
	MOV BL, 32
	MOV [C3], BL
	MOV BL, 31
	MOV [C4], BL
	MOV BL, 39
	MOV [C5], BL
	MOV BL, 31
	MOV [C6], BL
	MOV BL, 38
	MOV [C7], BL
	MOV BL, 35
	MOV [C8], BL
LOOP:
	IN 08
	CMP AL, 32
	JZ UP
	CMP AL, 34
	JZ LEFT
	CMP AL, 38
	JZ DOWN
	CMP AL, 36
	JZ RIGHT
	CMP AL, 35
	JZ STOP
	CMP AL, 0D
	JZ FINIS
	JMP LOOP

RIGHT:
	MOV AL, 1F
	OUT 04
	IN 08
;TULISAN KANAN
	MOV BL, 4B
	MOV [E6], BL
	MOV BL, 41
	MOV [E7], BL
	MOV BL, 4E
	MOV [E8], BL
	MOV BL, 41
	MOV [E9], BL
	MOV BL, 4E
	MOV [EA], BL
	JMP LOOP
LEFT:
	MOV AL, 2F
	OUT 04
	IN 08
;TULISAN KIRI
	MOV BL, 4B
	MOV [E6], BL
	MOV BL, 49
	MOV [E7], BL
	MOV BL, 52
	MOV [E8], BL
	MOV BL, 49
	MOV [E9], BL
	JMP LOOP

UP:
	MOV AL, 8F
	OUT 04
	IN 08
;TULISAN ATAS
	MOV BL, 41
	MOV [E6], BL
	MOV BL, 54
	MOV [E7], BL
	MOV BL, 41
	MOV [E8], BL
	MOV BL, 53
	MOV [E9], BL
	JMP LOOP
DOWN:
	MOV AL, 4F
	OUT 04
	IN 08
;TULISAN BAWAH
	MOV BL, 42
	MOV [E6], BL
	MOV BL, 41
	MOV [E7], BL
	MOV BL, 57
	MOV [E8], BL
	MOV BL, 41
	MOV [E9], BL
	MOV BL, 48
	MOV [EA], BL
	JMP LOOP
STOP:
	MOV AL, 00
	OUT 04
;TULISAN STOP
	MOV BL, 53
	MOV [E6], BL
	MOV BL, 54
	MOV [E7], BL
	MOV BL, 4F
	MOV [E8], BL
	MOV BL, 50
	MOV [E9], BL
	JMP LOOP
FINIS:
	MOV AL, FF
	OUT 04
;TULISAN FINIS
	MOV BL, 46
	MOV [E6], BL
	MOV BL, 49
	MOV [E7], BL
	MOV BL, 4E
	MOV [E8], BL
	MOV BL, 49
	MOV [E9], BL
	MOV BL, 53
	MOV [EA], BL
	JMP LOOP	
